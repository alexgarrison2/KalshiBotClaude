"""
Weather Edge Strategy — 4-layer signal model
═════════════════════════════════════════════

Trades Kalshi daily weather threshold markets (e.g., "Will Dallas high
exceed 89°F today?") by exploiting the gap between official forecasts
and what the market has priced.

LAYER 1 — METAR Intraday (highest priority)
  If the observed temperature already confirms the outcome, override
  model_prob to near-certainty (0.97 YES or 0.03 NO).
  Only valid for same-day markets — temperatures can't be "un-done".

LAYER 2 — NWS normCDF Base Model
  P(YES) = normCDF((NWS_forecast − threshold) / σ)
  σ = 3.0°F before 11 AM ET (more morning uncertainty)
  σ = 2.0°F after 11 AM ET  (tighter as the day resolves)

LAYER 3 — Ensemble Consensus (GFS + ECMWF via Open-Meteo)
  All 3 models agree directionally → +10¢ edge boost
  GFS/ECMWF disagree with NWS → skip trade entirely

LAYER 4 — Calibration Bias
  Historical finding: YES markets priced 35–65¢ win only 4–21% of the
  time on Kalshi weather markets (far below implied probability).
  When buying NO in this range → +15¢ edge credit.

Entry:   |effective_edge| > 20¢  AND  volume ≥ 1,000 contracts
         Market not closing within 60 minutes

Pricing:
  Edge ≥ 40¢ → PASSIVE  : limit at mid-price  (maker, zero fee)
  Edge 20–40¢ → MIDWAY  : limit 2¢ inside ask (likely maker)
  Unfilled 30+ min → CHASE: re-evaluate, fill at ask if still valid
"""
import math
